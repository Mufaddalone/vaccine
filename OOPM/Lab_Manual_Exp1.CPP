//Program to read two numbers and apply all types of operators.
#include<iostream>
using namespace std;

int main()
{
    int n1,n2;
    cout<<"Enter Two Number's:";
    cin>>n1>>n2;
    cout<<"Sum="<<n1+n2<<endl;
    cout<<"Difference="<<n1-n2<<endl;
    cout<<"Product="<<n1*n2<<endl;
    cout<<"Division="<<n1/n2<<endl;
    return 0;
}